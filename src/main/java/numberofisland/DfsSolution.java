    private static final char NOT_VISIT = '1';
    private static final char VISITED = '2';

    public int numIslands(char[][] grid) {
        int count = 0;
        for (int i = 0; i < grid.length; i++) {
            for (int j = 0; j < grid[i].length; j++) {
                if (grid[i][j] == NOT_VISIT) {
                    dfsVisit(grid, i, j);
                    count ++;
                }
            }
        }
        return count;
    }

    private void dfsVisit(char[][] grid, int row, int col) {
        if (grid[row][col] != NOT_VISIT) {
            return;
        }
        grid[row][col] = VISITED;
        if (row > 0) {
            dfsVisit(grid, row - 1, col);
        }
        if (row < grid.length - 1) {
            dfsVisit(grid, row + 1, col);
        }
        if (col > 0) {
            dfsVisit(grid, row, col - 1);
        }
        if (col < grid[row].length - 1) {
            dfsVisit(grid, row, col + 1);
        }
    }
